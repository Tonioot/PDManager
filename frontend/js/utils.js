/* ─── Status badge HTML ─────────────────────────────────────────────────── */
export function badge(status) {
  const labels = {
    running:  'Running',
    stopped:  'Stopped',
    deploying:'Deploying...',
    error:    'Error',
    starting: 'Starting…',
    stopping: 'Stopping…',
    restarting:'Restarting…',
  };
  return `<span class="badge badge-${status}">${labels[status] || 'Unknown'}</span>`;
}

/* ─── Toast notifications ───────────────────────────────────────────────── */
export function toast(msg, type = 'success') {
  const el = document.createElement('div');
  el.className = `toast ${type}`;
  el.textContent = msg;
  document.getElementById('toast-container').appendChild(el);
  setTimeout(() => el.remove(), 3200);
}

/* ─── Confirm dialog ────────────────────────────────────────────────────── */
export function confirm(title, body) {
  return new Promise(resolve => {
    const backdrop = document.createElement('div');
    backdrop.className = 'dialog-backdrop';
    backdrop.innerHTML = `
      <div class="dialog">
        <div class="dialog-title">${title}</div>
        <div class="dialog-body">${body}</div>
        <div class="dialog-actions">
          <button class="btn btn-secondary" id="dlg-cancel">Cancel</button>
          <button class="btn btn-danger" id="dlg-ok">Confirm</button>
        </div>
      </div>`;
    document.body.appendChild(backdrop);
    backdrop.querySelector('#dlg-cancel').onclick = () => { backdrop.remove(); resolve(false); };
    backdrop.querySelector('#dlg-ok').onclick    = () => { backdrop.remove(); resolve(true); };
    backdrop.addEventListener('click', e => { if (e.target === backdrop) { backdrop.remove(); resolve(false); } });
  });
}

